package type;

public class Location {
	float latitude;
	float intitude;
	public float getLatitude() {
		return latitude;
	}
	public void setLatitude(float latitude) {
		this.latitude = latitude;
	}
	public float getIntitude() {
		return intitude;
	}
	public void setIntitude(float intitude) {
		this.intitude = intitude;
	}
	
}
